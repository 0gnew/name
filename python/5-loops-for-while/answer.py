# Задание 3: Проверка на палиндром
def is_palindrome_while(s) -> bool:
    """
    Принимает строку s и возвращает True, если строка является палиндромом,
    и False в противном случае. Игнорируйте пробелы и регистр.
    Например, для s = "A man a plan a canal Panama", функция должна вернуть True.

    Палиндром — слово, которое одинаково читается и слева направо и справа налево,
    например, "поп" или "казак"
    """
    spaceless = s.replace(" ", "").lower()

    start_index = 0
    end_index = len(spaceless) - 1

    while start_index < end_index:
        if spaceless[start_index] != spaceless[end_index]:
            return False
        start_index += 1
        end_index -= 1

    return True

  
