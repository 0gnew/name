# Задание 1: Сумма чисел  
def sum_numbers_while(n) -> int:  
    """  
    Принимает целое число n и возвращает сумму всех чисел от 1 до n включительно.  
    Например, для n = 5, функция должна вернуть 15 (1+2+3+4+5).  
    """  
    result = 0 
    while n > 0: 
        result += n
        n -= 1
    return result
  
